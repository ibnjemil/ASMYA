/**
 * Look up a translation key for a given language.
 * Falls back to the key itself when no translation is found.
 *
 * @param lang  - The target language code (`en`, `am`, or `ar`).
 * @param key   - The dot-namespaced translation key (e.g. `'app.subtitle'`).
 * @returns The translated string, or `key` when missing.
 */
export function t(lang: Language, key: TranslationKey): string {
  return translations[lang]?.[key] ?? key
}
